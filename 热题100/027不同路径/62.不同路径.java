class Solution {
    public int uniquePaths(int m, int n) {
        //��̬�滮
        if (m == 0 || n == 0) return 0;
        if (m == 1 && n == 1) return 1;
        //��ά�����Ž��
        int[][] res = new int[m][n];
        //�Զ�ά����ı߽��и�ֵ
        for (int i = 0; i < m; i++) {
            res[i][0] = 1;
        }
        for (int j = 0; j < n; j++) {
            res[0][j] = 1;
        }
        //���ζԱ�������λ�ý��и�ֵ
        for (int i = 1; i < m; i++) {
            for (int j = 1; j < n; j++) {
                res[i][j] = res[i - 1][j] + res[i][j - 1];
            }
        }
        return res[m - 1][n - 1]; //���غ��½�λ��ֵ��Ϊ��Ӧ�Ľ��ֵ

    }
}