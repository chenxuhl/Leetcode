    public boolean canJump(int[] nums) {
        int ultraright = 0;  //���ܵ�����Զ�Ҷ�
        for (int i = 0; i < nums.length; i++) {
            if (i <= ultraright) {  //i���ܵ������Զ�Ҷ�֮�ڲſ��Լ�������
                ultraright = Math.max(ultraright, i + nums[i]);  //�����Ҷ���Զ��
                if (ultraright >= nums.length - 1) return true;  //�ж��Ƿ�������������������ǰ����ѭ��
            }
        }
        return false;  //����������Բ����㣬�򷵻�false

    }
